package messages.util;

public class CityConfig {
    public int clientID;
    public String clientName;
    public int x;
    public int y;

    public CityConfig(int clientID, String clientName, int x, int y) {
        this.clientID = clientID;
        this.clientName = clientName;
        this.x = x;
        this.y = y;
    }
}
